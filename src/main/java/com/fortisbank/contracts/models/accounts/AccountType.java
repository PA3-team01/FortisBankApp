package com.fortisbank.contracts.models.accounts;

/**
 * Enum representing different types of bank accounts.
 */
public enum AccountType {
    /**
     * Checking account type.
     */
    CHECKING,

    /**
     * Savings account type.
     */
    SAVINGS,

    /**
     * Credit account type.
     */
    CREDIT,

    /**
     * Currency account type.
     */
    CURRENCY
}